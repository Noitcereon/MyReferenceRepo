package com.experis.mytunesassignment.models;

public class MusicSearchInfo {
    public String artist;
    public String songTitle;
    public String genre;
    public String album;

    public MusicSearchInfo(String artist, String songTitle, String genre, String album) {
        this.artist = artist;
        this.songTitle = songTitle;
        this.genre = genre;
        this.album = album;
    }

}
